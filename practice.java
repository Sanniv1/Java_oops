public class practice {
    public static void main(String[] args){
        int x = 4;
        int y = 5;
        System.out.println("Before Swapping x:"+x+"y:"+y);
        // Swapping
        int temp=x;
        x=y;
        y=temp;
        System.out.println("After Swapping x:"+x+"y:"+y); 

    }
}
